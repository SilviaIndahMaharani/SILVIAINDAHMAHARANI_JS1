// Menggunakan while loop
let x = 1;
console.log("Menggunakan while loop:");
while (x <= 5) {
  console.log("Nilai x saat ini (while): " + x);
  x++;
}

// Menggunakan do-while loop
let y = 1;
console.log("Menggunakan do-while loop:");
do {
  console.log("Nilai y saat ini (do-while): " + y);
  y++;
} while (y <= 5);
